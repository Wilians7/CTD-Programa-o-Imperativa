//Crie uma função adicionar

function adicionar(a, b) {
    return a + b;
}

//Crie uma função Subtrair
function subtracao(a, b) {
    return a - b;
}

//Crie uma função Multiplicar
function multiplicacao(a, b) {
    return a * b;
}

//Crie uma função Dividir
function divisao(a, b) {
    return a / b;
}


console.log(adicionar(10, 20));
console.log(subtracao(30, 50));
console.log(multiplicacao(5, 9));
console.log(divisao(10, 5));